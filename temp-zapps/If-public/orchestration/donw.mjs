export default async function donw(_p) {
	// Initialisation of variables:

	const instance = await getContractInstance("TestShield");

	const contractAddr = await getContractAddress("TestShield");

	const msgValue = 0;
	const p = generalise(_p);

	// Send transaction to the blockchain:

	const txData = await instance.methods.donw(p).encodeABI();

	let txParams = {
		from: config.web3.options.defaultAccount,
		to: contractAddr,
		gas: config.web3.options.defaultGas,
		gasPrice: config.web3.options.defaultGasPrice,
		data: txData,
		chainId: await web3.eth.net.getId(),
	};

	const key = config.web3.key;

	const signed = await web3.eth.accounts.signTransaction(txParams, key);

	const tx = await web3.eth.sendSignedTransaction(signed.rawTransaction);

	const encEvent = {};

	return { tx, encEvent };
}
